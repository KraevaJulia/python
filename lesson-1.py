while (m >= 1) or (m_max < 9):
    m = m // 10 # Отбрасываем последнюю цифру
    if m % 10 > m_max: # Если остаток от деления на 10 числа m больше прошлого остатка,
        m_max = m % 10 # то присваем m_max значение нового остатка.
    if m > 9: #Если это не последний десяток числа, то продолжаем
        continue
    else:
        break
print('Самая большая цифра в числе ' + str(m_max))


