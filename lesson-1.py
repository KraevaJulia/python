#Задание 2
'''
Пользователь вводит время в секундах. 
Переведите время в часы, минуты, секунды и выведите в формате чч:мм:сс. Используйте форматирование строк.
'''
print('Задание 2')
print('Ведите время в секундах')
time = int(input())
hours = time//3600 #Делим нацело (60 секунд * 60 минут = 3600 секунд в одном часе)
minutes = (time - hours*3600)//60 #Высчитываем сколько минут и вычитаем кол-во часов
seconds = time - (hours*3600 + minutes*60) #Вычитаем часы и минуты получаем секунды.
print(f'Время в формате чч:мм:сс   {hours} : {minutes} : {seconds}') #Выводим результат

